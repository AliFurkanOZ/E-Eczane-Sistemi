/**
 * İl listesini döner
 */
export const getProvinces = () => {
    return Object.keys(addressData).sort();
};

/**
 * Seçilen ile göre ilçe listesini döner
 */
export const getDistricts = (province) => {
    if (!province || !addressData[province]) return [];
    return Object.keys(addressData[province]).sort();
};

/**
 * Seçilen il ve ilçeye göre mahalle listesini döner
 */
export const getNeighborhoods = (province, district) => {
    if (!province || !district || !addressData[province] || !addressData[province][district]) return [];
    return addressData[province][district].sort();
};

export default addressData;
